# A aula aborda os operadores relacionais
'''Esses operadores sao basicamente
OP          SIGNIFICADO         EXEMPLO (TRUE)
>           Maior               2 > 1
>=          Maior ou igual      2 >= 2
<           Menor               1 < 2
<=          Menor ou igual      1 <= 1
==          Igual               'a' == 'a'
!=          Diferente           'a' != 'a'
'''
maior = 2 > 1
print(maior)